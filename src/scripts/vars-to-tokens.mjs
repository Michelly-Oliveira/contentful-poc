import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

import Color from 'colorjs.io';

/**
 * Converts text to CSS-friendly variable names
 * For scales: extracts only the number (e.g., "50  - P Light" -> "50")
 * For color names: full slugification (e.g., "9fin Blue" -> "9fin-blue")
 */
function slugify(text, isScale = false) {
  if (isScale) {
    const scaleMatch = text.match(/^(\d+)/);
    if (scaleMatch) {
      return scaleMatch[1];
    }
  }

  return text
    .toLowerCase()
    .replace(/[^a-z0-9 -]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();
}

/**
 * Generates CSS custom properties from converted color tokens
 * Handles both color scales (objects) and single colors (strings)
 * Sorts scales numerically and adds line breaks between color groups
 * Adds --color- prefix for Tailwind compatibility
 */
function generateCssVariables(colorsObj) {
  let css = `/*\n * Auto-generated color tokens from Figma\n * Do not edit this file directly.\n * To modify colors, update the source in Figma or run the token generation script.\n * See /scripts/figma/README.md for more information.\n */\n\n@theme {\n`;
  let colorIndex = 0;

  for (const [colorName, colorValue] of Object.entries(colorsObj)) {
    if (typeof colorValue === 'object' && colorValue !== null) {
      // Handle color scales - sort by numeric value for consistent ordering
      const slugName = slugify(colorName);

      // Sort scales alphabetically by variable name
      const sortedScales = Object.entries(colorValue).sort(([a], [b]) => {
        const numA = parseInt(slugify(a, true)) || 0;
        const numB = parseInt(slugify(b, true)) || 0;
        // If both are numbers, sort numerically
        if (!isNaN(numA) && !isNaN(numB)) {
          return numA - numB;
        }
        // Otherwise, sort alphabetically
        return slugify(a).localeCompare(slugify(b));
      });

      for (const [scale, oklchValue] of sortedScales) {
        const cleanScale = slugify(scale, true);
        const cssVarName = `--color-${slugName}-${cleanScale}`;

        // Extract raw values from oklch() string
        const rawValue = oklchValue.replace(/^oklch\(|\)$/g, '');
        const rawVarName = `${cssVarName}-raw`;

        // Add raw value first, then wrapped value
        css += `  ${rawVarName}: ${rawValue};\n`;
        css += `  ${cssVarName}: ${oklchValue};\n`;
      }
    } else if (typeof colorValue === 'string') {
      // Handle single colors like "White", "Black", "9fin Blue"
      const slugName = slugify(colorName);
      const cssVarName = `--color-${slugName}`;

      // Extract raw values from oklch() string if it's an oklch color
      if (colorValue.startsWith('oklch(')) {
        const rawValue = colorValue.replace(/^oklch\(|\)$/g, '');
        const rawVarName = `${cssVarName}-raw`;

        // Add raw value first, then wrapped value
        css += `  ${rawVarName}: ${rawValue};\n`;
        css += `  ${cssVarName}: ${colorValue};\n`;
      } else {
        // For non-oklch values, just add as-is
        css += `  ${cssVarName}: ${colorValue};\n`;
      }
    }

    // Add visual separation between color groups
    colorIndex++;
    if (colorIndex < Object.keys(colorsObj).length) {
      css += '\n';
    }
  }

  css += '}';

  return css;
}

/**
 * Maps theme colors to their corresponding primitive color variable names
 * Reads original hex values from tokens.json to match against theme hex values
 * Returns --color- prefixed variable names for Tailwind compatibility
 */
function mapColorToVariable(hexValue) {
  // Read the original tokens to get hex values (not converted OKLCH values)
  const __filename = fileURLToPath(import.meta.url);
  const __dirname = dirname(__filename);
  const filePath = join(__dirname, 'tokens.json');
  const fileContent = readFileSync(filePath, 'utf-8');
  const tokens = JSON.parse(fileContent);

  const originalPrimitives = tokens.Primitives.Color;

  for (const [colorName, colorValue] of Object.entries(originalPrimitives)) {
    if (typeof colorValue === 'object' && colorValue !== null) {
      for (const [scale, value] of Object.entries(colorValue)) {
        if (value === hexValue) {
          const slugName = slugify(colorName);
          const cleanScale = slugify(scale, true);

          return `--color-${slugName}-${cleanScale}`;
        }
      }
    } else if (typeof colorValue === 'string' && colorValue === hexValue) {
      return `--color-${slugify(colorName)}`;
    }
  }

  return null; // Return null if no match found
}

/**
 * Generates theme CSS using CSS variable references
 * Creates [data-theme="light|dark"] selectors with --color- prefixed variables
 * Adds line breaks between categories for better readability
 */
function generateThemeCss(themeObj, themeName) {
  const themeClass =
    themeName === 'light'
      ? '.light,\n.light *:not(.dark),\n:root [data-theme="light"]'
      : '.dark,\n.dark *:not(.light),\n:root [data-theme="dark"]';
  let css = `${themeClass} {\n`;
  let categoryIndex = 0;

  for (const [category, colors] of Object.entries(themeObj)) {
    if (typeof colors === 'object' && colors !== null) {
      // Sort color entries alphabetically by color name
      const sortedColors = Object.entries(colors).sort(([a], [b]) => slugify(a).localeCompare(slugify(b)));

      for (const [colorName, hexValue] of sortedColors) {
        const variableName = mapColorToVariable(hexValue);

        if (variableName) {
          // Use direct CSS variable reference: var(--primitive-color)
          const cssVarName = `--color-${slugify(category)}-${slugify(colorName)}`;
          css += `  ${cssVarName}: var(${variableName});\n`;
        } else {
          // Fallback to direct OKLCH conversion if no primitive match found
          try {
            const color = new Color(hexValue);
            const oklchColor = color.to('oklch');
            const cssVarName = `--color-${slugify(category)}-${slugify(colorName)}`;
            css += `  ${cssVarName}: ${oklchColor.toString({
              precision: 3,
            })};\n`;
          } catch (error) {
            console.warn(`Failed to convert ${category}.${colorName}: ${hexValue}`, error);
          }
        }
      }

      // Add line break between categories (but not after the last one)
      categoryIndex++;
      if (categoryIndex < Object.keys(themeObj).length) {
        css += '\n';
      }
    }
  }

  css += '}';

  return css;
}

/**
 * Appends theme CSS to the ../../src/styles/theme/color.css file
 * Creates the directory if it doesn't exist
 * Maintains existing content and adds new themes with proper spacing
 */
function appendThemeToCssFile(themeCss) {
  const __filename = fileURLToPath(import.meta.url);
  const __dirname = dirname(__filename);
  const filePath = join(__dirname, '../../src/styles/theme/color.css');

  // Create directory if it doesn't exist
  const dir = dirname(filePath);
  if (!existsSync(dir)) {
    mkdirSync(dir, { recursive: true });
    console.log(`📁 Created directory: ${dir}`);
  }

  // Read existing CSS content
  const existingContent = readFileSync(filePath, 'utf-8');

  // Append theme CSS with spacing
  const updatedContent = existingContent + '\n\n' + themeCss;

  writeFileSync(filePath, updatedContent, 'utf-8');
  console.log(`🎨 Theme CSS appended to: ${filePath}`);
}

/**
 * Saves the generated CSS to ../../src/styles/theme/color.css
 * Creates the directory if it doesn't exist
 */
function saveCssFile(cssContent) {
  const __filename = fileURLToPath(import.meta.url);
  const __dirname = dirname(__filename);
  const filePath = join(__dirname, '../../src/styles/theme/color.css');

  // Create directory if it doesn't exist
  const dir = dirname(filePath);
  if (!existsSync(dir)) {
    mkdirSync(dir, { recursive: true });
    console.log(`📁 Created directory: ${dir}`);
  }

  writeFileSync(filePath, cssContent, 'utf-8');
  console.log(`💾 CSS variables saved to: ${filePath}`);
}

/**
 * Converts hex colors to OKLCH format
 * Handles both nested color scales and flat color values
 * Provides error handling for failed conversions
 */
function convertColorsToOklch(colorObj) {
  const converted = {};

  for (const [colorName, colorValue] of Object.entries(colorObj)) {
    if (typeof colorValue === 'object' && colorValue !== null) {
      // Process color scales (e.g., "Dark Blue": { "50": "#f2f6ff", ... })
      converted[colorName] = {};

      for (const [scale, hexValue] of Object.entries(colorValue)) {
        try {
          const color = new Color(hexValue);
          const oklchColor = color.to('oklch');
          converted[colorName][scale] = oklchColor.toString({
            precision: 3,
          });
        } catch (error) {
          console.warn(`Failed to convert ${colorName}.${scale}: ${hexValue}`, error);
          converted[colorName][scale] = hexValue;
        }
      }
    } else if (typeof colorValue === 'string') {
      // Process single colors (e.g., "White": "#ffffff")
      try {
        const color = new Color(colorValue);
        const oklchColor = color.to('oklch');
        converted[colorName] = oklchColor.toString({
          precision: 3,
        });
      } catch (error) {
        console.warn(`Failed to convert ${colorName}: ${colorValue}`, error);
        converted[colorName] = colorValue;
      }
    } else {
      // Keep non-string values as-is (shouldn't occur in this data structure)
      converted[colorName] = colorValue;
    }
  }

  return converted;
}

/**
 * Main function: parses Figma tokens, converts colors to OKLCH, and generates CSS
 * Processes primitives, then generates light/dark themes with variable references
 */
export function fetchAndParseFigmaTokens() {
  try {
    const __filename = fileURLToPath(import.meta.url);
    const __dirname = dirname(__filename);
    const filePath = join(__dirname, 'tokens.json');
    const fileContent = readFileSync(filePath, 'utf-8');
    const tokens = JSON.parse(fileContent);

    // Process Primitives.Color section
    if (tokens.Primitives && tokens.Primitives.Color) {
      const originalColors = tokens.Primitives.Color;
      const convertedColors = convertColorsToOklch(originalColors);
      tokens.Primitives.Color = convertedColors;

      console.log('🎨 Converted colors to OKLCH format:');
      console.log(JSON.stringify(tokens.Primitives.Color, null, 2));

      // Generate and save primitive CSS variables
      const cssVariables = generateCssVariables(convertedColors);
      console.log('\n🎯 Generated CSS variables:');
      console.log(cssVariables);

      saveCssFile(cssVariables);

      // Process themes and combine them in a single @layer base block
      let combinedThemeCss = '@layer base {\n';

      // Process Mapped/Light theme
      if (tokens['Mapped/Light']) {
        console.log('\n🌞 Processing Light theme...');
        const lightThemeCss = generateThemeCss(tokens['Mapped/Light'], 'light');
        console.log('Light theme CSS:');
        console.log(lightThemeCss);
        combinedThemeCss += lightThemeCss + '\n\n';
      }

      // Process Mapped/Dark theme
      if (tokens['Mapped/Dark']) {
        console.log('\n🌙 Processing Dark theme...');
        const darkThemeCss = generateThemeCss(tokens['Mapped/Dark'], 'dark');
        console.log('Dark theme CSS:');
        console.log(darkThemeCss);
        combinedThemeCss += darkThemeCss + '\n';
      }

      combinedThemeCss += '}';
      appendThemeToCssFile(combinedThemeCss);
    }

    return tokens;
  } catch (error) {
    console.error('Error fetching and parsing Figma tokens:', error);
    throw error;
  }
}

// Execute when run directly
fetchAndParseFigmaTokens();
